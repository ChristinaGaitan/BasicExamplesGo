// Valores

package main

import "fmt"

func main() {

	// Concatenacion
	fmt.Println("go" + "lang")
	fmt.Println("5 + 5 =", 5+5)

	// Manejo de flotantes
	fmt.Println("7.5/3.2 = ", 7.5/3.2)

	// Manejo de booleanos
	fmt.Println("Booleanos = ")
	fmt.Println("true AND false = ", true && false)
	fmt.Println("true OR false = ", true || false)
	fmt.Println("NOT false = ", !false)
}